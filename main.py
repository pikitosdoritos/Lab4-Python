# вивести елементи з непарними індексами;
# вивести елементи з початку і до 5-го включно, з 3-го і до
# передостаннього включно з кроком 2;
# вивести елементи списку у зворотному порядку із використанням
# слайдів та із використанням методів списків;
# поміняти місцями перший та останній елементи списку із
